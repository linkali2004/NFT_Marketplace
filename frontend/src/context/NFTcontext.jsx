const getEthereumContract = async () => {
    const provider = new ethers.JsonRpcProvider("https://sepolia.infura.io/v3/403d8f6a043c45fa9e93f33926ac8a69");
    const wallet = new ethers.Wallet("085abe8ddc67e57f657fbed18e49e558dca4493fa2fa86a9ba0fc8c0dbb4c6dd", provider);
    const contract = new ethers.Contract("0xcA11282800C8757e86e869e772692899D87444D8", abi, wallet);
    return new Promise(function (resolve, reject) {
        resolve({ provider, wallet, contract });
    })
}




